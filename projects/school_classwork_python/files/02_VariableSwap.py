a = int(input("Original value of a: "))
b = int(input("Original value of b: "))

# Swapping variables
temp = a
a = b
b = temp

# Showing output
print()
print("--------------------------------------")
print()
print("The new values of a and b after swapping are:")
print("a = ", str(a))
print("b = ", str(b))